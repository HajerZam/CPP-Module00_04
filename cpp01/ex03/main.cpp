#include "HumanA.hpp"
#include "HumanB.hpp"

int main()
{
	// Human A uses a reference to Weapon, so we must create the Weapon first
	{
		Weapon club = Weapon("crude spiked club");
		HumanA bob("Bob", club);
		bob.attack();
		club.setType("some other type of club");
		bob.attack();
	}
	// Human B uses a pointer to Weapon, so we can create it without a weapon and set it later
	{
		Weapon club = Weapon("crude spiked club");
		HumanB jim("Jim");
		jim.setWeapon(club);
		jim.attack();
		club.setType("some other type of club");
		jim.attack();
	}
	return 0;
}
